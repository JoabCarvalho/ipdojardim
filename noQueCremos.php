        <h3>ORIGEM</h3>
            <p>Credo Apostólico, o mais conhecido dos credos, é atribuído pela tradição aos doze apóstolos. Mas os estudiosos acreditam que ele se desenvolveu a partir de pequenas confissões batismais empregadas nas igrejas dos primeiros séculos. Embora os seus artigos sejam de origem bem antiga, acredita-se atualmente que o credo apostólico só alcançou sua forma definitiva por volta do sexto século, quando são encontrados registros do seu emprego na liturgia oficial da igreja ocidental. De um modo ou de outro, parece evidente sua conexão com outros credos antigos menores; como os seguintes:</p>
